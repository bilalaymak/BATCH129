package day11forloop;

public class NestedForloop05 {
    public static void main(String[] args) {

/*
    Example 1: Asagidaki ciktiyi verecek kodu yaziniz ( bir ay icinde 4 hafta 7 gun )
                Week: 1
                  Day: 1
                  Day: 2
                  Day: 3
                  .....
                Week: 2
                  Day: 1
                  Day: 2
                  Day: 3
                  ....
                Week: 3
                  Day: 1
                  Day: 2
                  Day: 3
                  ....
*/

        for(int i =1; i<5; i++){

            System.out.println("Week: " + i);

            for(int k = 1; k<8; k++){

                System.out.println("Day : " +k);

            } // 4 * 7 olasilik durumu 1*7 + 1* 7 + 1*7 + 1*7
            //Dünya nın kendi(inner) ve gunes(outer)  etrafında donmesi gibi






        }









    }
}
