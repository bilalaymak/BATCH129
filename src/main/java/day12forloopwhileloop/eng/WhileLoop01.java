package day12forloopwhileloop.eng;

public class WhileLoop01 {
    public static void main(String[] args) {

        //example 1: type code to print integers from 4 to 7 on the console

        //1. way: use for-loop
        for (int i = 4;i < 8; i++){
            System.out.print(i + " ");
        }

        System.out.println();
        //2. way: use while-loop
        int i = 4;
        while (i<8){
            System.out.print(i + " ");
            i++;
        }






    }
}








