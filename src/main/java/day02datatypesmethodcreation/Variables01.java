package day02datatypesmethodcreation;

public class Variables01 {
    public static void main(String[] args) {
        /*gömlek ve ayakkabı fiyatları için iki tane variable oluşturup toplam fiyatı ekrana
        yazdırın
        */

        float gomlekFiyat = 299.99F;
        float ayakkabiFiyat = 999.99F;

        System.out.println(gomlekFiyat + ayakkabiFiyat);

        /* hücre ağırlığı ve amipin ağırlığı için iki tane variable oluşturun ve
        ağırlıkları farkını ekrana yazdırın
         */

        double hucreAgirilgi = 0.00000000000000112;
        double amipAgirligi = 0.000000000000000012;

        System.out.println(hucreAgirilgi - amipAgirligi);






    }
}
