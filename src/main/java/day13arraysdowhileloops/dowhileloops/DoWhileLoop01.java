package day13arraysdowhileloops.dowhileloops;

public class DoWhileLoop01 {
    public static void main(String[] args) {

        //1) while loop


        int i =1;

        while (i<1){//while body hic calismadi-zero execution!!
            //while loop da zero execution mumkundur

            System.out.println("sen bir while loop'sun");
            i++;
        }


        //2) do-while-loop

        int k = 1;

        do {

            System.out.println("sen bir do while loop'sun");
            k++;
        }while (k<1);




    }
}
